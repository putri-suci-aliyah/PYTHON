def hitung_jumlah_tuple(tup):
    total = sum(tup)
    return total

# Contoh tuple
contoh_tuple = (1, 2, 3, 4, 5)

# Memanggil fungsi un/tuk menghitung jumlah tuple
hasil_jumlah = hitung_jumlah_tuple(contoh_tuple)

# Menampilkan hasil
print(f"Tuple: {contoh_tuple}")
print(f"Jumlah elemen dalam tuple: {hasil_jumlah}")
